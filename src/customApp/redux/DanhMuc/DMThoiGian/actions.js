const actions = {
  THOIGIAN_GET_INIT_DATA_REQUEST:
    'THOIGIAN_GET_INIT_DATA_REQUEST',
  THOIGIAN_GET_INIT_DATA_REQUEST_SUCCESS:
    'THOIGIAN_GET_INIT_DATA_REQUEST_SUCCESS',
  THOIGIAN_GET_INIT_DATA_REQUEST_ERROR:
    'THOIGIAN_GET_INIT_DATA_REQUEST_ERROR',
  THOIGIAN_GET_INIT_DATA_REQUEST_ERROR:
    'THOIGIAN_GET_INIT_DATA_REQUEST_ERROR',
  getInitData: (filterData) => {
    return (disPatch, getState) => {
      //get role
      // -------
      disPatch({
        type: actions.THOIGIAN_GET_INIT_DATA_REQUEST,
        payload: {filterData},
      });
    };
  },
  THOIGIAN_GET_INIT_DATA_REQUEST_ERROR: 'THOIGIAN_GET_INIT_DATA_REQUEST_ERROR',
  getData: (filterData) => {
    return (disPatch, getState) => {
      //get role
      // -------
      disPatch({
        type: actions.THOIGIAN_GET_INIT_DATA_REQUEST,
        payload: {filterData},
      });
    };
  },
  getData: (filterData) => ({
    type: actions.THOIGIAN_GET_INIT_DATA_REQUEST,
    payload: {filterData},
  }),

  THOIGIAN_GET_ALL_DATA_REQUEST: 'THOIGIAN_GET_ALL_DATA_REQUEST',
  THOIGIAN_GET_ALL_DATA_REQUEST_SUCCESS:
    'THOIGIAN_GET_ALL_DATA_REQUEST_SUCCESS',
  THOIGIAN_GET_ALL_DATA_REQUEST_ERROR: 'THOIGIAN_GET_ALL_DATA_REQUEST_ERROR',
  getAll: (filterData) => ({
    type: actions.THOIGIAN_GET_ALL_DATA_REQUEST,
    payload: {filterData},
  }),
};

export default actions;
